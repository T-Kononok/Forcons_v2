package data;

public class YX {

    private Integer y;
    private Integer x;

    public YX(Integer y, Integer x) {
        this.y = y;
        this.x = x;
    }

    public YX() {
        this.y = 0;
        this.x = 0;
    }

    public Integer getY() {
        return y;
    }

    public Integer getX() {
        return x;
    }

    public void setY(Integer y) {
        this.y = y;
    }

    public void setX(Integer x) {
        this.x = x;
    }
}
